/*          Estructuras en la Programacion

Secuencia:   
Son series de acciones desarrolladas en un orden específico,
ya que se realiza una acción tras otra hasta que todas se hayan llevado a cabo.

Condicionales:
En lugar de seguir un orden específico de eventos, 
realiza una preguntar para determinar el camino a seguir.

Ciclos:
Así como los condicionales, los ciclos también realizan preguntas.
La diferencia es que hacen la misma pregunta una y otra vez hasta que cierta tarea haya sido elaborada.
*/

